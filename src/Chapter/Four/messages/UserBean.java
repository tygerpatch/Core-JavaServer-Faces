package Chapter.Four.messages;

public class UserBean {
  private String name;
  private int age;

  public String getName() {
    return name;
  }

  public void setName(String newValue) {
    name = newValue;
  }

  public int getAge() {
    return age;
  }

  public void setAge(int newValue) {
    age = newValue;
  }
}