package Chapter.One;

public class UserBean {
  private String name;
  private String password;

  public String getName() {
    return name;
  }

  public void setName(String newValue) {
    name = newValue;
  }

  public String getPassword() {
    return password;
  }

  public void setPassword(String newValue) {
    password = newValue;
  }
}
